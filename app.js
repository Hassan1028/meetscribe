// ─────────────────────────────────────────────────────────────────────────────
//  STATE
// ─────────────────────────────────────────────────────────────────────────────
let audioCapture   = null;
let speechMic      = null;
let isRecording    = false;
let timerInterval  = null;
let elapsedSeconds = 0;
let segmentCount   = 0;
let generatedMarkdown = "";
let finalCombinedTranscript = "";
let sessionStart   = null;
let hasMic = false;
let hasTab = false;

// ─────────────────────────────────────────────────────────────────────────────
//  INIT — Load saved settings
// ─────────────────────────────────────────────────────────────────────────────
(async () => {
  const savedKey  = await storageGet("gemini_api_key");
  const savedLang = await storageGet("recognition_lang");
  if (savedKey)  apiKeyInput.value = savedKey;
  if (savedLang) langSelect.value  = savedLang;

  if (!SpeechTranscriber.isSupported()) {
    showAlert("⚠️ Web Speech API not available. Please use Chrome.", "error");
  }

})();

// ─────────────────────────────────────────────────────────────────────────────
//  UI HELPERS
// ─────────────────────────────────────────────────────────────────────────────
function setStatus(state, text) {
  statusBadge.className = `status-badge ${state}`;
  statusText.textContent = text;
}

function showAlert(msg, type = "warning") {
  alertBanner.className = `alert-banner ${type} show`;
  alertTextEl.textContent = msg;
}

function hideAlert() {
  alertBanner.className = "alert-banner";
}

function showLoading(text, sub) {
  loadingText.textContent = text;
  loadingSub.textContent  = sub;
  loadingOverlay.classList.add("show");
}

function hideLoading() {
  loadingOverlay.classList.remove("show");
}

function updateClock(seconds) {
  sessionClock.textContent = formatDuration(seconds);
  sessionClock.classList.toggle("active", isRecording);
}

function updateStats(transcript) {
  const words = transcript.trim() === "" ? 0 :
    transcript.trim().split(/\s+/).filter(w => !["[MIC]","[TAB]"].includes(w)).length;
  statWords.textContent     = words;
  statDuration.textContent  = `${Math.floor(elapsedSeconds / 60)}m`;
  statSegments.textContent  = segmentCount;
  statSource.textContent    = hasMic && hasTab ? "2" : hasMic ? "1" : "—";
}

function updateTranscriptDisplay(full, interim) {
  transcriptEmpty.style.display = "none";

  // Format [MIC] and [TAB] labels with color spans
  const formatted = full
    .replace(/\[MIC\]/g, '<span class="label-mic">[MIC]</span>')
    .replace(/\[TAB\]/g, '<span class="label-tab">[TAB]</span>');

  transcriptEl.innerHTML = formatted +
    (interim ? `<span class="interim-text"> ${interim}…</span>` : "");

  // Auto-scroll
  const area = document.getElementById("transcript-area");
  area.scrollTop = area.scrollHeight;

  updateStats(full);
}

