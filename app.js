// ─────────────────────────────────────────────────────────────────────────────
//  START RECORDING
// ─────────────────────────────────────────────────────────────────────────────
btnStart.addEventListener("click", async () => {
  hideAlert();

  // Validate API key early
  const apiKey = apiKeyInput.value.trim();
  if (!apiKey) {
    showAlert("Please enter your Gemini API key in Settings before starting.", "error");
    return;
  }

  // Save key in case user hadn't focused away
  await storageSet("gemini_api_key", apiKey);
  await storageSet("recognition_lang", langSelect.value);

  setStatus("processing", "Initializing…");
  btnStart.disabled = true;

  // ── Reset state ──
  if (speechMic) speechMic.reset();
  transcriptEl.innerHTML = "";
  minutesEl.innerHTML    = "";
  transcriptEmpty.style.display  = "flex";
  minutesEmpty.style.display     = "flex";
  generatedMarkdown = "";
  finalCombinedTranscript = "";
  segmentCount = 0;
  elapsedSeconds = 0;
  hasMic = false;
  hasTab = false;
  updateClock(0);
  updateStats("");

  audioCapture = new AudioCapture();

  // ── Step 1: Mic — request permission and grab label, then release the stream
  // so Web Speech API has exclusive hardware access (holding getUserMedia open
  // simultaneously causes silence on Windows WASAPI).
  let micLabel = "Microphone";
  try {
    const micStream = await audioCapture.captureMic();
    micLabel = micStream.getAudioTracks()[0]?.label || "Microphone";
    // Release immediately — Web Speech API manages its own mic access
    micStream.getTracks().forEach(t => t.stop());
    audioCapture.micStream = null;
    hasMic = true;
    micStatus.textContent = "Active";
    micStatus.className   = "source-pill active";
    micDetail.textContent = micLabel;
  } catch (err) {
    showAlert(err.message, "error");
    setStatus("error", "Error");
    btnStart.disabled = false;
    return;
  }

  // ── Step 2: Tab Audio (optional, via getDisplayMedia dialog) ──
  let tabStream = null;

  if (tabAudioToggle.checked) {
    setStatus("processing", "Waiting for tab selection…");
    showAlert("📌 Chrome's share dialog is opening — select your tab and tick 'Share tab audio'.", "info");
    try {
      tabStream = await audioCapture.captureTabWithDialog();
    } catch (err) {
      if (err.message !== "Tab audio capture was cancelled.") {
        showAlert(`⚠️ ${err.message}`, "warning");
      } else {
        hideAlert();
      }
    }
  }

  if (tabStream) {
    hasTab = true;
    tabStatus.textContent = "Active";
    tabStatus.className   = "source-pill active";
    tabDetail.textContent = tabStream.getAudioTracks()[0]?.label || "Tab Audio";
    audioCapture.startTabRecorder(tabStream);
    hideAlert();
    showAlert("✅ Mic + Tab audio captured. Recording both sources.", "success");
  } else {
    tabStatus.textContent = "Off";
    tabStatus.className   = "source-pill inactive";
    tabDetail.textContent = tabAudioToggle.checked ? "Cancelled — mic only" : "Not enabled";
    if (!tabAudioToggle.checked) hideAlert();
  }

  sessionStart = new Date();

  // ── Step 3: Start Speech Recognition ──
  // Brief pause so the released mic stream's hardware lock clears before Web Speech API claims it
  await new Promise(r => setTimeout(r, 250));

  speechMic = new SpeechTranscriber();
  speechMic.label = "[MIC]";

  speechMic.onTranscriptUpdate = (full, interim) => {
    segmentCount++;
    updateTranscriptDisplay(full, interim);
  };

  speechMic.onNoSpeech = () => {
    // Show a transient hint so the user knows the mic is active but not hearing audio
    setStatus("listening", "Listening… (no audio detected yet)");
    setTimeout(() => { if (isRecording) setStatus("listening", "Listening…"); }, 2000);
  };

  speechMic.onError = (msg) => {
    console.error("[MeetScribe]", msg);
    showAlert(`Recognition error: ${msg}`, "error");
  };

  try {
    speechMic.start(langSelect.value);
  } catch (err) {
    showAlert(err.message, "error");
    setStatus("error", "Error");
    btnStart.disabled = false;
    await audioCapture.stopAll();
    return;
  }

  // ── Step 4: Timer ──
  isRecording = true;
  timerInterval = setInterval(() => {
    elapsedSeconds++;
    updateClock(elapsedSeconds);
    updateStats(speechMic?.getTranscript() || "");
  }, 1000);

  // ── Update UI ──
  setStatus("listening", "Listening…");
  waveIndicator.classList.add("active");
  btnStart.disabled      = true;
  btnStop.disabled       = false;
  btnGenerate.disabled   = true;
  btnDownload.disabled   = true;
  btnTranscript.disabled = true;

  footerInfo.textContent = `Recording started · ${formatTimestamp(sessionStart)}`;
});

// ─────────────────────────────────────────────────────────────────────────────
//  STOP RECORDING
// ─────────────────────────────────────────────────────────────────────────────
btnStop.addEventListener("click", async () => {
  if (!isRecording) return;
  isRecording = false;

  clearInterval(timerInterval);
  waveIndicator.classList.remove("active");
  setStatus("processing", "Stopping…");
  btnStop.disabled = true;

  // Stop speech recognition — get final mic transcript
  const micTranscript = speechMic ? speechMic.stop() : "";

  // Stop audio streams — get tab audio blob if captured
  const tabAudioBlob = await audioCapture.stopAll();

  // Reset source pills
  micStatus.textContent = "Done";
  micStatus.className   = "source-pill inactive";
  tabStatus.textContent = hasTab ? "Done" : "N/A";
  tabStatus.className   = "source-pill inactive";

  // Transcribe tab audio with Gemini if we have it
  let tabTranscript = "";
  if (hasTab && tabAudioBlob && tabAudioBlob.size > 0) {
    setStatus("processing", "Transcribing tab audio…");
    showAlert("🎧 Transcribing tab/meeting audio with Gemini…", "info");
    try {
      const apiKey = apiKeyInput.value.trim();
      const rawTab = await transcribeTabAudio(apiKey, tabAudioBlob);
      if (rawTab) {
        // Label each sentence with [TAB]
        tabTranscript = rawTab
          .split(/(?<=[.?!])\s+/)
          .map(s => `[TAB] ${s.trim()}`)
          .join(" ");
      }
    } catch (err) {
      console.warn("[MeetScribe] Tab transcription error:", err.message);
      showAlert(`⚠️ Tab audio transcription failed: ${err.message}`, "warning");
    }
  }

  // Merge mic + tab transcripts
  const combined = [micTranscript, tabTranscript].filter(Boolean).join(" ");
  const finalTranscript = cleanTranscript(combined);
  finalCombinedTranscript = finalTranscript;

  // Update display with final transcript
  updateTranscriptDisplay(finalTranscript, "");
  updateStats(finalTranscript);

  if (!finalTranscript || finalTranscript.length < 10) {
    setStatus("error", "No Speech Detected");
    showAlert("No speech was detected. Make sure your microphone is working, or enable tab audio sharing for browser/Zoom audio.", "error");
    btnStart.disabled      = false;
    btnGenerate.disabled   = true;
    btnTranscript.disabled = true;
    return;
  }

  setStatus("done", "Ready");
  hideAlert();
  showAlert(`✅ Recording complete. ${finalTranscript.split(" ").length} words captured. Click "Generate Minutes" to create your MoM.`, "success");

  btnStart.disabled      = false;
  btnGenerate.disabled   = false;
  btnDownload.disabled   = true;
  btnTranscript.disabled = false;

  footerInfo.textContent = `Recording stopped · Duration: ${formatDuration(elapsedSeconds)}`;
});

// ─────────────────────────────────────────────────────────────────────────────
//  GENERATE MINUTES
// ─────────────────────────────────────────────────────────────────────────────
btnGenerate.addEventListener("click", async () => {
  const apiKey = apiKeyInput.value.trim();
  if (!apiKey) {
    showAlert("Gemini API key is required. Enter it in Settings.", "error");
    return;
  }

  const transcript = finalCombinedTranscript
    || speechMic?.fullTranscript?.trim()
    || transcriptEl.innerText?.trim()
    || "";

  if (!transcript || transcript.length < 20) {
    showAlert("Transcript is too short. Please record a longer meeting.", "error");
    return;
  }

  hideAlert();
  showLoading("Generating Minutes of Meeting…", "Sending transcript to Gemini 1.5 Flash 8B");
  btnGenerate.disabled = true;
  setStatus("processing", "AI Processing…");

  try {
    const meetingDate = formatTimestamp(sessionStart || new Date());
    const durationMin = Math.ceil(elapsedSeconds / 60) || 1;

    generatedMarkdown = await generateMinutes(apiKey, transcript, meetingDate, durationMin);

    // Switch to minutes tab
    document.querySelector('[data-tab="minutes"]').click();

    minutesEmpty.style.display = "none";
    minutesEl.innerHTML = renderMarkdown(generatedMarkdown);

    hideLoading();
    setStatus("done", "Done ✓");
    hideAlert();
    showAlert("✅ Minutes generated successfully! Click 'Download .md' to save.", "success");

    btnDownload.disabled = false;
    btnGenerate.disabled = false;
    footerInfo.textContent = `MoM generated · ${formatTimestamp(new Date())}`;

  } catch (err) {
    hideLoading();
    setStatus("error", "AI Error");
    showAlert(`Gemini error: ${err.message}`, "error");
    btnGenerate.disabled = false;
    console.error("[MeetScribe] Gemini error:", err);
  }
});

// ─────────────────────────────────────────────────────────────────────────────
//  DOWNLOAD
// ─────────────────────────────────────────────────────────────────────────────
btnDownload.addEventListener("click", () => {
  if (!generatedMarkdown) {
    showAlert("No minutes to download. Generate minutes first.", "warning");
    return;
  }

  downloadFile(generatedMarkdown, "minute_of_meeting.md", "text/markdown");
  showAlert("✅ Downloading minute_of_meeting.md …", "success");
});

btnTranscript.addEventListener("click", () => {
  const transcript = finalCombinedTranscript
    || speechMic?.fullTranscript?.trim()
    || transcriptEl.innerText?.trim()
    || "";

  if (!transcript) {
    showAlert("No transcript to download. Record a meeting first.", "warning");
    return;
  }

  const timestamp = formatTimestamp(sessionStart || new Date());
  const content = `MeetScribe — Transcript\n${timestamp}\n${"=".repeat(40)}\n\n${transcript}`;
  downloadFile(content, "transcript.txt", "text/plain");
  showAlert("✅ Downloading transcript.txt …", "success");
});

// ─────────────────────────────────────────────────────────────────────────────
//  SAFETY — warn before closing tab during recording
// ─────────────────────────────────────────────────────────────────────────────
window.addEventListener("beforeunload", (e) => {
  if (isRecording) {
    e.preventDefault();
    e.returnValue = "Recording is in progress. Are you sure you want to leave?";
  }
});
